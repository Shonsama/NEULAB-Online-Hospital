package com.neuedu.lab.model.po;

public class DiseaseInfo {
    private String disease_id;
    private String disease_type;
    private String disease_name;
    private String disease_icd;

    public String getDisease_id() {
        return disease_id;
    }

    public void setDisease_id(String disease_id) {
        this.disease_id = disease_id;
    }

    public String getDisease_type() {
        return disease_type;
    }

    public void setDisease_type(String disease_type) {
        this.disease_type = disease_type;
    }

    public String getDisease_name() {
        return disease_name;
    }

    public void setDisease_name(String disease_name) {
        this.disease_name = disease_name;
    }

    public String getDisease_icd() {
        return disease_icd;
    }

    public void setDisease_icd(String disease_icd) {
        this.disease_icd = disease_icd;
    }
}
