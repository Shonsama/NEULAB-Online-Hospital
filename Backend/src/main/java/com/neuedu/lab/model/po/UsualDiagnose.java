package com.neuedu.lab.model.po;

public class UsualDiagnose {
    private Integer usual_diagnose_id;
    private String usual_diagnose_disease_name;
    private String usual_diagnose_result;
    private Integer usual_diagnose_doctor_id;

    public Integer getUsual_diagnose_id() {
        return usual_diagnose_id;
    }

    public void setUsual_diagnose_id(Integer usual_diagnose_id) {
        this.usual_diagnose_id = usual_diagnose_id;
    }

    public String getUsual_diagnose_disease_name() {
        return usual_diagnose_disease_name;
    }

    public void setUsual_diagnose_disease_name(String usual_diagnose_disease_name) {
        this.usual_diagnose_disease_name = usual_diagnose_disease_name;
    }

    public String getUsual_diagnose_result() {
        return usual_diagnose_result;
    }

    public void setUsual_diagnose_result(String usual_diagnose_result) {
        this.usual_diagnose_result = usual_diagnose_result;
    }

    public Integer getUsual_diagnose_doctor_id() {
        return usual_diagnose_doctor_id;
    }

    public void setUsual_diagnose_doctor_id(Integer usual_diagnose_doctor_id) {
        this.usual_diagnose_doctor_id = usual_diagnose_doctor_id;
    }
}
